        <h1 class="font-semibold text-xl">AVISO DE PRIVACIDAD</h1>


        <p class="font-semibold">I. Denominación y domicilio del responsable</p>


        <p>El Laboratorio Estatal de Salud Pública, con domicilio en calle privada de Canadá número 94 del
            fraccionamiento Las Américas CP 58270 en Morelia Michoacán, es el responsable del uso y protección de los
            datos que usted proporcione; lo anterior de conformidad con el artículo 3° Fracción II, 16 y 22 en relación
            con el 25 al 29 de la Ley General de Protección de Datos Personales en Posesión de los Sujetos Obligados y
            8°, 21 al 25, 27, 40, 42, 43 y 45 de la Ley de Protección de Datos Personales en Posesión de Sujetos
            Obligados del Estado de Michoacán de Ocampo.</p>



        <p class="font-semibold">II. Finalidad de la obtención de datos personales</p>



        <p>Los datos que recabamos de usted serán utilizados para fines médicos de apoyo diagnóstico, de vigilancia
            epidemiológica, de protección contra riesgos sanitarios, investigación y en su caso, para trámites
            administrativos y laborales.</p>



        <p>Le informamos que al interior del laboratorio usted es videograbado por cámaras de seguridad, las imágenes
            captadas serán utilizadas únicamente con fines de preservar la SEGURIDAD de las instalaciones del
            laboratorio, del personal institucional, proveedores y visitantes, y serán utilizadas para mantener el
            control de las entradas y salidas de quienes ingresan a las instalaciones del laboratorio.</p>



        <p class="font-semibold">III. Tipo de datos sometidos a tratamiento</p>



        <p>· Los datos personales que serán sometidos a tratamiento son: Número de afiliación al Sistema de Protección
            Social en Salud o Derechohabiencia, Nombre, Edad, Sexo, Domicilio, Teléfono particular, Teléfono celular,
            Firma, Nombre de familiares, Escolaridad, Clave de Registro Federal de Contribuyentes (RFC), Lugar de
            nacimiento, Fecha de nacimiento, Correo electrónico e imagen.</p>





        <p>· Para la realización de trámites administrativos y laborales los datos personales y sensibles son: nombre,
            edad, Sexo, domicilio, teléfono particular, teléfono celular, firma, clave de Registro Federal de
            Contribuyentes (RFC), Clave Única de Registro de Población (CURP), Clave del Instituto Nacional Electoral,
            Número de Pasaporte, lugar de nacimiento, fecha de nacimiento, correo electrónico, puesto o cargo que
            desempeña, afiliación sindical, estado civil, idioma, nombre de familiares, trayectoria educativa, títulos,
            cédula profesional, certificados, reconocimientos, referencias laborales, referencias personales, trabajos
            anteriores, documentos de reclutamiento y selección de personal.</p>



        <h2 class="font-semibold">IV. Transferencia de datos</h2>



        <p>Los datos personales que poseemos están protegidos de accesos no autorizados, podrán ser transferidos a
            Servicios de Salud de Michoacán, Régimen Estatal de Protección Social en Salud (SEGURO POPULAR), ISSSTE,
            IMSS, COEPRIS, COFEPRIS, InDRE, CCAyAC, HOSPITALES Y JURISDICCIONES (Primer y Segundo Nivel de Atención
            Medica), así como Laboratorios Estatales de todo el territorio Nacional, SEDENA, SEMAR y SECRETARIA DE SALUD
            FEDERAL entre otros. Se transmiten al emitir resultados de pruebas analíticas de muestras humanas y
            ambientales para la realización de fines médicos de apoyo diagnóstico, de vigilancia epidemiológica, de
            protección contra riesgos sanitarios e investigación y en su caso, para trámites administrativos y
            laborales.</p>



        <p>La transferencia de datos personales y sensibles para la realización de fines médicos de apoyo diagnóstico,
            de vigilancia epidemiológica, de protección contra riesgos sanitarios e investigación, y en su caso, para
            trámites administrativos y laborales, y en casos excepcionales, a diversas instituciones públicas de los
            sectores de salud nacionales e internacionales, para contribuir a la protección de la salud de la población;
            por lo que no se requiere el consentimiento del titular cuando implique el cumplimiento de una disposición
            legal o en el ejercicio de dichas atribuciones conferidas por la ley.</p>



        <p>En el caso de los datos laborales, éstos podrán ser transferidos eventualmente a las autoridades competentes
            en materia de seguridad social y/o autoridades laborales, en cumplimiento a las disposiciones que en dichas
            materias rigen.</p>



        <h2 class="font-semibold">V. Fundamento Legal</h2>



        <p>Artículos 3° Fracción II, 16 y 25 al 29 de la Ley General de Protección de Datos Personales en Posesión de
            los Sujetos Obligados y 8°, 21 al 25, 27,40,42,43 y 45 de la Ley de Protección de Datos Personales en
            Posesión de Sujetos Obligados del Estado de Michoacán de Ocampo.</p>



        <p class="font-semibold">VI. ¿Cómo Acceder, Rectificar, Cancelar u Oponerse al uso y tratamiento de sus datos
            personales (Derechos
            ARCO) o revocar su consentimiento para el tratamiento sus datos?</p>

        <p>Usted tiene derecho de acceso, rectificación, cancelación u oposición al tratamiento de sus datos personales
            o revocar el consentimiento. Para el ejercicio de éstos derechos el titular de los datos personales o su
            representante deberán presentar solicitud de ejercicio de derechos ARCO, en formato libre siempre que reúna
            los siguientes requisitos:

            a) Acreditar que es el titular de los datos personales ante la autoridad a la que se dirige la solicitud.

            b) Nombre, datos generales e identificación oficial del solicitante, o en su defecto poder otorgado por el
            titular de los datos personales.

            c) Precisión de los datos respecto de los que busca ejercer alguno de los derechos ARCO (Acceso,
            Rectificación, Cancelación y Oposición).

            d) Domicilio para recibir notificaciones y/o correo electrónico.

            e) Modalidad en la que prefiere se le otorgue el acceso a sus datos (verbalmente, mediante consulta directa,
            a través de documentos como copias simples, certificadas u otros).



            f) Algún elemento que facilite la localización de la información.

            g) Firma del solicitante.</p>



        <p>Le informamos que adicionalmente puede presentar su solicitud de protección de datos personales vía
            electrónica al correo: direccionlespmich@gmail.com o bien puede acudir directamente a la Unidad de
            Transparencia de la Secretaría de Salud con domicilio en Allende # 505, Centro Histórico, Morelia,
            Michoacán, en un horario de lunes a viernes de 9:00 a 15:00 horas.</p>



        <p>También podrá ejercer sus derechos mediante solicitud de protección de datos personales, la cual podrá
            ingresar a través de la Plataforma Nacional de Transparencia, a través del portal:
            https://www.plataformadetransparencia.org.mx/web/guest/inicio</p>

        <h2 class="font-semibold">VII. Mecanismos de seguridad</h2>



        <p>La Secretaría de Salud de Michoacán, a través del Laboratorio Estatal de Salud Pública, emplea
            procedimientos físicos, electrónicos y administrativos para prevenir el acceso no autorizado, mantener la
            exactitud de los datos, y garantizar el uso correcto de su información personal.</p>



